@dataclass
class ValidationResult:
    """Validation output separated into blocking errors and advisory warnings."""

    errors: list[str]
    warnings: list[str]


def validate_repository(root: Path = ROOT) -> ValidationResult:
    """Return all source invariant errors and warnings for *root*."""
    errors: list[str] = []
    warnings: list[str] = []
    _set_root(root)
    _validate_skill_registry(errors)
    capability_entries = _validate_capability_registry(errors, warnings)
    _validate_capability_references(capability_entries, errors, warnings)
    _validate_domain_extensions(errors)
    _validate_source_runtime_boundary(errors, warnings)
    _validate_hook_runtime_consistency(errors, warnings)
    _validate_validation_broker_mapping(errors)
    _validate_memory_and_graph_coverage(errors)
    return ValidationResult(errors=errors, warnings=warnings)


