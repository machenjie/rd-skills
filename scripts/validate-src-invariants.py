def _load_registry(path: Path, expected_kind: str, context: str, errors: list[str]) -> Any:
    if not path.is_file():
        errors.append(f"{context}: registry file is missing")
        return {}
    try:
        data = load_yaml_file(path)
    except ValidationProblem as exc:
        errors.append(_clean_validation_error(exc))
        return {}
    if not isinstance(data, dict):
        errors.append(f"{context}: registry must be a mapping")
        return {}
    if data.get("schema_version") is None:
        errors.append(f"{context}: schema_version is required")
    if data.get("kind") is None:
        errors.append(f"{context}: kind is required")
    elif data.get("kind") != expected_kind:
        errors.append(f"{context}: kind must be {expected_kind}")
    return data


def _registry_names(path: Path, key: str) -> set[str]:
    try:
        data = load_yaml_file(path)
    except ValidationProblem:
        return set()
    names: set[str] = set()
    for entry in registry_items(data, key, path, []):
        if isinstance(entry, dict) and isinstance(entry.get("name"), str):
            names.add(entry["name"])
    return names


def _import_module(
    path: Path,
    name: str,
    *,
    extra_paths: list[Path] | None = None,
    package_name: str | None = None,
) -> ModuleType | None:
    if not path.is_file():
        return None
    inserted: list[str] = []
    for extra in extra_paths or []:
        value = str(extra)
        if value not in sys.path:
            sys.path.insert(0, value)
            inserted.append(value)
    spec_name = package_name or name
    try:
        spec = importlib.util.spec_from_file_location(spec_name, path)
        if spec is None or spec.loader is None:
            return None
        module = importlib.util.module_from_spec(spec)
        sys.modules[spec.name] = module
        spec.loader.exec_module(module)
        return module
    except Exception:
        return None
    finally:
        for value in inserted:
            try:
                sys.path.remove(value)
            except ValueError:
                pass


def _required_text(entry: dict[str, Any], field: str, context: str, errors: list[str]) -> str | None:
    value = entry.get(field)
    if not isinstance(value, str) or not value.strip():
        errors.append(f"{context}: field {field!r} must be a non-empty string")
        return None
    return value.strip()


def _check_unique(
    seen: dict[str, int],
    value: str,
    context: str,
    field: str,
    errors: list[str],
) -> None:
    if value in seen:
        errors.append(f"{context}: duplicate {field} {value!r}; first seen at index {seen[value]}")
    else:
        match = re.search(r"\[(\d+)\]$", context)
        seen[value] = int(match.group(1)) if match else -1


def _runtime_or_generated_path(path_value: str) -> bool:
    normalized = path_value.replace("\\", "/").strip("/")
    return normalized.startswith("dist/") or "/dist/" in normalized or "generated" in normalized


def _nonempty_field(value: Any) -> bool:
    if isinstance(value, str):
        return bool(value.strip())
    return _nonempty_string_list(value)


def _nonempty_string_list(value: Any) -> bool:
    return isinstance(value, list) and bool(value) and all(
        isinstance(item, str) and item.strip() for item in value
    )


def _visible_dirs(root: Path) -> list[Path]:
    if not root.is_dir():
        return []
    return [
        path
        for path in sorted(root.iterdir())
        if path.is_dir() and not path.name.startswith(".")
    ]


