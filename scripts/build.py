@dataclass(frozen=True)
class RuntimeItem:
    name: str
    path: Path
    kind: str
    description: str
    version: str
    metadata: dict[str, Any]
    body: str


@dataclass(frozen=True)
class Capability(RuntimeItem):
    capability_id: str
    group: str
    used_by: tuple[str, ...]
    triggers: tuple[str, ...]
    risk_notes: tuple[str, ...]
    expected_outputs: tuple[str, ...]


def main() -> int:
    parser = argparse.ArgumentParser(description="Build ChangeForge runtime artifacts.")
    parser.add_argument(
        "--profile",
        choices=PROFILES,
        default="recommended",
        help="Runtime profile to build.",
    )
    args = parser.parse_args()

    try:
        build_result = build_profile(args.profile)
    except BuildError as exc:
        print(f"build: ERROR: {exc}", file=sys.stderr)
        return 1

    print(
        "build: built profile "
        f"{args.profile} with {build_result['professional_count']} professional skill(s), "
        f"{build_result['compiled_capability_count']} compiled foundation capability reference(s), "
        f"{build_result['domain_extension_count']} domain extension(s), "
        f"and {build_result['zip_count']} OpenAI API zip(s)."
    )
    return 0


class BuildError(Exception):
    """Raised when build inputs are malformed or unsafe."""


def build_profile(profile: str) -> dict[str, int]:
    registries = _load_registries()
    professional_skills = _load_runtime_items(
        registries["skills"],
        expected_kind="professional-skill",
    )
    domain_extensions = _load_runtime_items(
        registries["domain_extensions"],
        expected_kind="domain-extension",
    )
    capabilities = _load_capabilities(registries["capabilities"])
    routing_rules = registries["routing_rules"]
    stage_model = _load_stage_model_registry()

    top_level_items = _top_level_items(profile, professional_skills, capabilities, domain_extensions)
    compiled_capability_names = _build_all_runtime_roots(
        profile,
        top_level_items,
        professional_skills,
        capabilities,
        domain_extensions,
        routing_rules,
    )
    _build_hook_runtime(professional_skills, capabilities, domain_extensions, stage_model)
    _cleanup_legacy_openai_zips(OPENAI_ZIP_DIR)
    zip_count = _package_openai_zips(
        UNIVERSAL_SKILLS_ROOT / profile,
        OPENAI_ZIP_DIR / profile,
    )

    return {
        "professional_count": len(professional_skills),
        "compiled_capability_count": len(compiled_capability_names),
        "domain_extension_count": len(domain_extensions),
        "zip_count": zip_count,
    }


def _load_registries() -> dict[str, list[dict[str, Any]]]:
    registry_specs = {
        "skills": ("skills.yaml", "skills"),
        "capabilities": ("capabilities.yaml", "capabilities"),
        "domain_extensions": ("domain-extensions.yaml", "domain_extensions"),
        "routing_rules": ("routing-rules.yaml", "routing_rules"),
    }
    registries: dict[str, list[dict[str, Any]]] = {}

    for logical_name, (file_name, key) in registry_specs.items():
        path = REGISTRY_DIR / file_name
        if not path.is_file():
            raise BuildError(f"missing registry file: {path.relative_to(ROOT)}")
        data = load_yaml_file(path)
        if not isinstance(data, dict):
            raise BuildError(f"{path.relative_to(ROOT)} must be a mapping")
        entries = data.get(key, [])
        if not isinstance(entries, list):
            raise BuildError(f"{path.relative_to(ROOT)} field '{key}' must be a list")
        normalized: list[dict[str, Any]] = []
        for index, entry in enumerate(entries):
            if not isinstance(entry, dict):
                raise BuildError(f"{file_name}:{key}[{index}] must be a mapping")
            normalized.append(entry)
        registries[logical_name] = normalized

    return registries


def _load_stage_model_registry() -> dict[str, Any]:
    path = REGISTRY_DIR / "stage-model.yaml"
    if not path.is_file():
        raise BuildError(f"missing registry file: {path.relative_to(ROOT)}")
    data = load_yaml_file(path)
    if not isinstance(data, dict):
        raise BuildError(f"{path.relative_to(ROOT)} must be a mapping")
    if data.get("kind") != "changeforge.stage_model":
        raise BuildError(f"{path.relative_to(ROOT)} kind must be changeforge.stage_model")
    return data


def _load_runtime_items(
    entries: list[dict[str, Any]],
    expected_kind: str,
) -> list[RuntimeItem]:
    items: list[RuntimeItem] = []
    seen_names: set[str] = set()

    for index, entry in enumerate(entries):
        name = _required_string(entry, "name", f"{expected_kind}[{index}]")
        rel_path = _required_string(entry, "path", f"{expected_kind}[{index}]")
        source_path = ROOT / rel_path
        item = _load_runtime_item(name, source_path, expected_kind)
        if item.name in seen_names:
            raise BuildError(f"duplicate runtime item name: {item.name}")
        seen_names.add(item.name)
        items.append(item)

    return items


def _load_runtime_item(name: str, source_path: Path, expected_kind: str) -> RuntimeItem:
    skill_file = source_path / "SKILL.md"
    if not skill_file.is_file():
        raise BuildError(f"{source_path.relative_to(ROOT)} is missing SKILL.md")

    try:
        metadata, _raw_frontmatter, body = parse_frontmatter(skill_file)
    except ValidationProblem as exc:
        raise BuildError(str(exc).replace(str(ROOT) + "/", "")) from exc

    actual_name = metadata.get("name")
    if actual_name != name:
        raise BuildError(f"{skill_file.relative_to(ROOT)} name does not match registry entry")

    kind = metadata.get("changeforge_kind")
    if kind != expected_kind:
        raise BuildError(
            f"{skill_file.relative_to(ROOT)} kind must be {expected_kind}, got {kind!r}"
        )

    description = metadata.get("description")
    version = metadata.get("changeforge_version")
    if not isinstance(description, str) or not description.strip():
        raise BuildError(f"{skill_file.relative_to(ROOT)} is missing description")
    if not isinstance(version, str) or not version.strip():
        raise BuildError(f"{skill_file.relative_to(ROOT)} is missing changeforge_version")

    return RuntimeItem(
        name=name,
        path=source_path,
        kind=expected_kind,
        description=description.strip(),
        version=version.strip(),
        metadata=metadata,
        body=body,
    )


def _load_capabilities(entries: list[dict[str, Any]]) -> list[Capability]:
    capabilities: list[Capability] = []
    seen_names: set[str] = set()
    seen_ids: set[str] = set()

    for index, entry in enumerate(entries):
        context = f"capabilities[{index}]"
        name = _required_string(entry, "name", context)
        rel_path = _required_string(entry, "path", context)
        base = _load_runtime_item(name, ROOT / rel_path, "foundation-capability")
        capability_id = _required_string(entry, "id", context)
        group = _required_string(entry, "group", context)

        if capability_id in seen_ids:
            raise BuildError(f"duplicate capability id: {capability_id}")
        if name in seen_names:
            raise BuildError(f"duplicate capability name: {name}")
        seen_ids.add(capability_id)
        seen_names.add(name)

        capabilities.append(
            Capability(
                name=base.name,
                path=base.path,
                kind=base.kind,
                description=base.description,
                version=base.version,
                metadata=base.metadata,
                body=base.body,
                capability_id=capability_id,
                group=group,
                used_by=tuple(_string_list(entry.get("used_by"), context, "used_by")),
                triggers=tuple(_string_list(entry.get("triggers"), context, "triggers")),
                risk_notes=tuple(_string_list(entry.get("risk_notes"), context, "risk_notes")),
                expected_outputs=tuple(
                    _string_list(entry.get("expected_outputs"), context, "expected_outputs")
                ),
            )
        )

    return capabilities


def _required_string(entry: dict[str, Any], key: str, context: str) -> str:
    value = entry.get(key)
    if not isinstance(value, str) or not value.strip():
        raise BuildError(f"{context}: missing string field '{key}'")
    return value.strip()


def _string_list(value: Any, context: str, key: str) -> list[str]:
    if not isinstance(value, list) or not all(isinstance(item, str) and item.strip() for item in value):
        raise BuildError(f"{context}: field '{key}' must be a list of strings")
    return [item.strip() for item in value]


def _top_level_items(
    profile: str,
    professional_skills: list[RuntimeItem],
    capabilities: list[Capability],
    domain_extensions: list[RuntimeItem],
) -> list[RuntimeItem]:
    if profile == "recommended":
        return [*professional_skills]
    if profile == "full":
        return [*professional_skills, *domain_extensions]
    if profile == "dev":
        return [*professional_skills, *capabilities, *domain_extensions]
    raise BuildError(f"unsupported profile: {profile}")


def _build_all_runtime_roots(
    profile: str,
    top_level_items: list[RuntimeItem],
    professional_skills: list[RuntimeItem],
    capabilities: list[Capability],
    domain_extensions: list[RuntimeItem],
    routing_rules: list[dict[str, Any]],
) -> set[str]:
    capability_map = _capabilities_by_professional_skill(capabilities)
    compiled_capability_names: set[str] = set()

    for root in (UNIVERSAL_SKILLS_ROOT, *AGENT_SKILL_ROOTS):
        profile_root = root / profile
        _reset_dir(profile_root)

        for item in top_level_items:
            destination = profile_root / item.name
            _copy_skill_tree(item.path, destination)
            if item.kind == "professional-skill":
                skill_capabilities = capability_map.get(item.name, [])
                _write_capability_references(destination, skill_capabilities)
                compiled_capability_names.update(capability.name for capability in skill_capabilities)
                if item.name == "change-forge-router":
                    _write_router_indexes(
                        destination,
                        professional_skills,
                        capabilities,
                        domain_extensions,
                        routing_rules,
                    )

        _write_build_manifest(
            profile_root,
            profile,
            top_level_items,
            professional_skills,
            capabilities,
            domain_extensions,
            compiled_capability_names,
        )

    return compiled_capability_names


def _capabilities_by_professional_skill(
    capabilities: list[Capability],
) -> dict[str, list[Capability]]:
    mapped: dict[str, list[Capability]] = {}
    for capability in capabilities:
        for skill_name in capability.used_by:
            mapped.setdefault(skill_name, []).append(capability)

    for skill_name, skill_capabilities in mapped.items():
        mapped[skill_name] = sorted(
            skill_capabilities,
            key=lambda capability: (
                capability.group,
                _capability_sort_id(capability),
                capability.name,
            ),
        )
    return mapped


def _capability_sort_id(capability: Capability) -> int:
    try:
        return int(capability.capability_id)
    except ValueError:
        return 0


def _reset_dir(path: Path) -> None:
    if path.exists():
        shutil.rmtree(path)
    path.mkdir(parents=True, exist_ok=True)


def _copy_skill_tree(source: Path, destination: Path) -> None:
    def ignore(_directory: str, names: list[str]) -> set[str]:
        return {
            name
            for name in names
            if name in {".DS_Store", "__pycache__"} or name.endswith((".pyc", ".pyo"))
        }

    if destination.exists():
        shutil.rmtree(destination)
    shutil.copytree(source, destination, ignore=ignore)


