def _render_routing_rules(routing_rules: list[dict[str, Any]]) -> str:
    lines = [GENERATED_MARKER, "", "# Routing Rules", ""]
    for entry in sorted(
        routing_rules,
        key=lambda item: (-int(item.get("priority", 0)), str(item.get("route_to", ""))),
    ):
        route_to = entry.get("route_to", "")
        priority = entry.get("priority", "")
        lines.extend([f"## {route_to}", "", f"- Priority: {priority}"])
        triggers = entry.get("triggers", [])
        if isinstance(triggers, list) and triggers:
            lines.append("- Triggers:")
            lines.extend(f"  - {trigger}" for trigger in triggers if isinstance(trigger, str))
        lines.append("")
    return "\n".join(lines)


