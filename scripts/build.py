def _write_capability_references(destination: Path, capabilities: list[Capability]) -> None:
    references_dir = destination / "references" / "capabilities"
    references_dir.mkdir(parents=True, exist_ok=True)

    for stale_path in references_dir.iterdir():
        if stale_path.is_file() and stale_path.suffix == ".md":
            stale_path.unlink()
        elif stale_path.is_dir():
            shutil.rmtree(stale_path)

    index_lines = [
        GENERATED_MARKER,
        "",
        "# Compiled Foundation Capabilities",
        "",
    ]

    if not capabilities:
        index_lines.extend(
            [
                "No foundation capabilities are directly assigned to this professional skill.",
                "",
            ]
        )
    else:
        for capability in capabilities:
            capability_reference_stem = f"{capability.capability_id}-{capability.name}"
            file_name = f"{capability_reference_stem}.md"
            index_lines.append(
                f"- [{capability.capability_id} {capability.name}]({file_name})"
            )
            (references_dir / file_name).write_text(
                _render_capability_reference(
                    capability,
                    runtime_reference_stem=capability_reference_stem,
                ),
                encoding="utf-8",
            )
            _copy_capability_owned_references(
                capability,
                references_dir / capability_reference_stem / "references",
            )
        index_lines.append("")

    (references_dir / "index.md").write_text("\n".join(index_lines), encoding="utf-8")


def _render_capability_reference(
    capability: Capability,
    *,
    runtime_reference_stem: str,
) -> str:
    sections = _extract_sections(
        capability.body,
        (
            "Mission",
            "When To Use",
            "Non-Negotiable Rules",
            "Industry Benchmarks",
            "Helm Chart Release Controls",
            "Helm Pipeline Gate",
            "Selection Rules",
            "Risk Escalation Rules",
            "Critical Details",
            "Failure Modes",
            "Output Contract",
            "Evidence Contract",
            "Quality Gate",
            "Completion Criteria",
        ),
    )

    lines = [
        GENERATED_MARKER,
        "",
        f"# {capability.name}",
        "",
        f"- Capability ID: {capability.capability_id}",
        f"- Group: {capability.group}",
        f"- Version: {capability.version}",
        f"- Used by: {', '.join(capability.used_by)}",
        f"- Triggers: {'; '.join(capability.triggers)}",
        f"- Risk notes: {'; '.join(capability.risk_notes)}",
        f"- Expected outputs: {'; '.join(capability.expected_outputs)}",
        "",
    ]

    for title, content in sections.items():
        lines.extend(
            [
                f"## {title}",
                "",
                _rewrite_capability_runtime_links(content.strip(), runtime_reference_stem),
                "",
            ]
        )

    return "\n".join(lines)


def _rewrite_capability_runtime_links(content: str, runtime_reference_stem: str) -> str:
    return re.sub(
        r"(?P<prefix>\]\()(?P<target>(?:\./)?references/[^)\s]+)(?P<suffix>\))",
        lambda match: (
            f"{match.group('prefix')}{runtime_reference_stem}/"
            f"{match.group('target').removeprefix('./')}{match.group('suffix')}"
        ),
        content,
    )


def _copy_capability_owned_references(capability: Capability, destination: Path) -> None:
    source_references = capability.path / "references"
    if not source_references.is_dir():
        return
    for source in sorted(source_references.rglob("*")):
        if not source.is_file():
            continue
        relative = source.relative_to(source_references)
        target = destination / relative
        target.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(source, target)


def _extract_sections(body: str, wanted_titles: tuple[str, ...]) -> dict[str, str]:
    wanted = {title.casefold(): title for title in wanted_titles}
    sections: dict[str, list[str]] = {}
    current_title: str | None = None

    for line in body.splitlines():
        match = re.match(r"^\s{0,3}#{1,6}\s+(.+?)\s*#*\s*$", line)
        if match:
            normalized = match.group(1).strip().casefold()
            current_title = wanted.get(normalized)
            if current_title and current_title not in sections:
                sections[current_title] = []
            continue

        if current_title:
            sections[current_title].append(line)

    return {
        title: "\n".join(sections[title]).strip()
        for title in wanted_titles
        if title in sections and "\n".join(sections[title]).strip()
    }


